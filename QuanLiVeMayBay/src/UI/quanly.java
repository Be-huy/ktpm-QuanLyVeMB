private void chucVu() {
        if (Auth.user == null) {
            lblChucVu.setText("Chức Vụ");
            lblHoTen.setText("Họ Tên");
        } else {
            String cv = Auth.user.getChucVu()? "Trưởng Phòng" : "Nhân Viên";
            String hoTen = Auth.user.getTenNV();
            lblChucVu.setText(cv);
            lblHoTen.setText(hoTen);
        }
    }
